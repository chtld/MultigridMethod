function [ Ih ] = I_h_2h( n )
m = n;
Ih = zeros((m/2-1)*(n/2-1), (m-1)*(n-1));
for i=1: 1: n/2-1
    for j=1: 1: n/2-1
        Ih((n/2-1)*(i-1)+j,((n-1)*(2*i-2)+2*j)-1:((n-1)*(2*i-2)+2*j)+1)=1/16*[1 2 1];
        Ih((n/2-1)*(i-1)+j,((n-1)*(2*i-1)+2*j)-1:((n-1)*(2*i-1)+2*j)+1)=1/16*[2 4 2];
        Ih((n/2-1)*(i-1)+j,((n-1)*(2*i)+2*j)-1:((n-1)*(2*i)+2*j)+1)=1/16*[1 2 1];
    end
end
end

